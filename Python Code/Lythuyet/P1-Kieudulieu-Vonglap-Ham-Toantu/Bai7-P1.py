#BAI 7: CAC HAM PHO BIEN TRONG PYTHON

from  math import* #can dong lenh nay de su dung cac ham toan hoc

#1. Ham sqrt
"""
ham sqrt (square root-can bac) tra ve so thuc hay con goi la so float

"""
print(sqrt(35))

#2. Ham isqrt
"""
ham isqrt (integer square root-can so nguyen ) tra ve so nguyen hay con goi la so int
"""

print(isqrt(35))

#3. Ham pow
"""
ham pow (power-mu) tra ve so thuc hay con goi la so float
"""
print(pow(2,10))
print(pow(10,1/3))

#4. Ham ceil
"""
ham ceil (ceiling-Tran nha) tra ve so gan nhat lon hon hoac bang so hien tai
"""

print(ceil(2.3))
print(ceil(7.8))

#5. Ham floor
"""
ham floor-san nha tra ve so gan nhat nho hon hoac bang so hien tai
"""
print(floor(2.1))
print(floor(1.2))

#6. Ham factorial
"""
ham factorial-giai thua 
"""
print(factorial(4))

#7. Ham gcd(a,b)
"""
ham gcd(greatest common divisor-Uoc chung lon nhat)
"""
print(gcd(100,450))

#8. Ham comb(n,k)
"""
ham tinh to hop chap k cua n
"""

print(comb(10,2))

#9. Ham perm(n,k)
"""
ham chinh hop chap k cua n
"""
print(perm(10,2))

#10. Ham fabs()
"""
ham fabs() ham tinh tri tuyet doi
"""
print(fabs(-100))

#11. Ham round()
"""
ham round la ham lam tron phu thuoc vao phan thap phan
"""

#12. Ham max, min, sum

print(max(1,2,3,4))
print(min(1,-2,5))
print(sum([1,2,1]))

